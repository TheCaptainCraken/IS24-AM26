package it.polimi.ingsw.model;

/**
 * The Kingdom enum represents the different types of kingdoms that a card can belong to in the game.
 * Each kingdom corresponds to a different type of nature element.
 *
 * PLANT: Represents the plant kingdom.
 * ANIMAL: Represents the animal kingdom.
 * FUNGI: Represents the fungi kingdom.
 * INSECT: Represents the insect kingdom.
 */
public enum Kingdom {
    PLANT,
    ANIMAL,
    FUNGI,
    INSECT
}
